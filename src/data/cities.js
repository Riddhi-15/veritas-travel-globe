// Helper: normalize city name to key format
// "New York City" → "NEW_YORK", "Chiang Mai" → "CHIANG_MAI"
export function cityToKey(iso2, cityName) {
  const name = cityName
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '') // strip diacritics
    .replace(/\bCity\b|\bMunicipality\b|\bProvince\b/gi, '') // strip generic suffixes
    .trim()
    .toUpperCase()
    .replace(/[^A-Z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
  return `${iso2.toUpperCase()}_${name}`
}

